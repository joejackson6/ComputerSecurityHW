def generate_decryption_round_keys(key_bin):
    key_56 = apply_permutation(key_bin, parity_drop)
    left_key = key_56[:28]
    right_key = key_56[28:]
    round_keys = []
    print("Generating round keys:")
    for i in range(16):
        left_key = circular_left_shift(left_key, shift_schedule[i])
        right_key = circular_left_shift(right_key, shift_schedule[i])
        combined_key = left_key + right_key
        round_key = apply_permutation(combined_key, key_compress)
        round_keys.append(round_key)
        print("Round", i+1, ":", round_key)
    return round_keys[::-1]

def des_decrypt_binary(cipher_bin, key_bin):
    print("Ciphertext:", cipher_bin)
    permuted_text = apply_permutation(cipher_bin, ip_map)
    print("After initial permutation:", permuted_text)
    left_part = permuted_text[:32]
    right_part = permuted_text[32:]
    print("L0:", left_part)
    print("R0:", right_part)
    decryption_keys = generate_decryption_round_keys(key_bin)
    for round_num in range(16):
        print("\n--- Round", round_num+1, "---")
        expanded_right = apply_permutation(right_part, expansion_box)
        print("Expanded R:", expanded_right)
        xored = xor_bin(expanded_right, decryption_keys[round_num])
        print("XOR with key:", xored)
        sbox_result = ""
        for j in range(8):
            six_bits = xored[j*6:(j+1)*6]
            row = int(six_bits[0] + six_bits[-1], 2)
            col = int(six_bits[1:5], 2)
            sbox_value = s_boxes[j][row][col]
            sbox_result += format(sbox_value, '04b')
        print("S-box output:", sbox_result)
        permuted_sbox = apply_permutation(sbox_result, straight_pbox)
        print("After P-box:", permuted_sbox)
        new_right = xor_bin(left_part, permuted_sbox)
        print("New R:", new_right)
        left_part, right_part = right_part, new_right
        print("New L:", left_part)
    combined = right_part + left_part
    print("\nBefore final permutation:", combined)
    plain_bin = apply_permutation(combined, fp_map)
    print("After final permutation:", plain_bin)
    return plain_bin

def binary_to_english(binary_str):
    if len(binary_str) % 8 != 0:
        raise ValueError("Binary string length must be a multiple of 8.")
    english_text = ""
    for i in range(0, len(binary_str), 8):
        byte = binary_str[i:i+8]
        english_text += chr(int(byte, 2))
    return english_text

