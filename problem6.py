def apply_permutation(bitstr, perm_list):
    return ''.join(bitstr[i - 1] for i in perm_list)

def circular_left_shift(bitstr, shift_amt):
    return bitstr[shift_amt:] + bitstr[:shift_amt]

def xor_bin(a, b):
    return ''.join('0' if a[i] == b[i] else '1' for i in range(len(a)))

